    private void configuraFileChooser(){
        fileChooser.setDialogTitle("Escolha uma imagem");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Imagens", "jpg", "png", "gif", "bmp"));
    }
    
    private void abrirArquivo(){
        if(fileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION)
        {
            try{
                File arquivo = fileChooser.getSelectedFile();
            imagemSelecionada = ImageIO.read(arquivo);
            colocarImagem();
            
            } catch(Exception e){
                e.printStackTrace();
            }
            
        }
    }
    
    private void colocarImagem(){
        imagemTamanho = imagemSelecionada.getScaledInstance(imagemPainel.getWidth(), imagemPainel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon icone = new ImageIcon(imagemTamanho);
        imagem.setIcon(icone);
    }
    
    private void limparImagem(){
        if(imagem.getIcon() != null){
            imagem.setIcon(null);
        } else {
            JOptionPane.showMessageDialog(this, "Imagem ainda n√£o selecionada");
        }
    }
    
   

